package michail.JSONtokens;

// Enumeration to represent different types of tokens
public enum TokenType {
    LEFT_BRACE,     // {
    RIGHT_BRACE,    // }
    LEFT_BRACKET,   // [
    RIGHT_BRACKET,  // ]
    COLON,          // :
    COMMA,          // ,
    TRUE,           // true
    FALSE,          // false
    NULL,           // null
    STRING,         // string
    NUMBER,          // number
    WHITESPACE
}

